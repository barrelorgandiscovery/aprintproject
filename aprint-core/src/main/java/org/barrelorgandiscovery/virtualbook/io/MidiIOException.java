package org.barrelorgandiscovery.virtualbook.io;

public class MidiIOException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 3656550622002447598L;

	public MidiIOException() {
		super();
		
	}

	public MidiIOException(String arg0) {
		super(arg0);
		
	}

	public MidiIOException(String arg0, Throwable arg1) {
		super(arg0, arg1);
		
	}

	public MidiIOException(Throwable arg0) {
		super(arg0);
		
	}

}
