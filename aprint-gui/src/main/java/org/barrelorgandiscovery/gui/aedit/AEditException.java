package org.barrelorgandiscovery.gui.aedit;

public class AEditException extends Exception {

	public AEditException() {
		
	}

	public AEditException(String message) {
		super(message);
	}

	public AEditException(Throwable cause) {
		super(cause);
	}

	public AEditException(String message, Throwable cause) {
		super(message, cause);
	}

}
